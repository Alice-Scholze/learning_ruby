# Vamos ver como setar o tipo de número que queremos

# Binário: coloque 0b antes de declarar o número
puts 0b10

# Octal: coloque 0o antes de declarar o número
puts 0o45

# Hexadecimal: coloque 0x antes de declarar o número
puts 0xAD123

# Se tivermos um número extenso e quisermos facilitar a leitura, podemos separar em 'blocos' com '_':
puts 1_000_000

# Aqui alguns tipos de números
puts 1.class
puts 10101010101010101010.class
puts 1.5.class